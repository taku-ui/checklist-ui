// -----------------------------
// HTML要素取得
// -----------------------------
const container = document.getElementById("weaponList");
const search = document.getElementById("search");

// -----------------------------
// localStorageから状態を読み込み
// -----------------------------16:58 2026/03/25
let unlocked = JSON.parse(localStorage.getItem("unlocked")) || {};



// >>ここが追加：コンプリート率更新用関数<<
// -----------------------------
function updateCompletion() { 
  const total = weapons.length;

  const count = weapons.filter(name => unlocked[name]).length;
  const percent = Math.round((count / total) * 100);

  const completionDiv = document.getElementById("completion");
  console.log("updateCompletion called", count, total, percent, completionDiv); // ←追加
  completionDiv.textContent = `取得済み: ${count} / ${total} (${percent}%)`;

  // >>ここが追加：全取得時の演出（1度だけ）<<
  if (count === total && !completionDiv.dataset.completed) {
    completionDiv.dataset.completed = "true";
    alert("コンプリート！！！");
  }
}



// -----------------------------
// 武器リスト表示関数
// filter で検索にも対応
// -----------------------------
function renderList(filter = "") {
  container.innerHTML = ""; // 一旦リセット

  weapons
    .filter(name => name.includes(filter)) // 検索対応
    .forEach(name => {
      const div = document.createElement("div");

      // クラスで管理
      div.classList.add("weapon");

      // 取得済みか未取得かでクラスを付ける
      if (unlocked[name]) {
        div.classList.add("unlocked");
      } else {
        div.classList.add("locked");
      }

      // 表示は画像のみ
      div.innerHTML = `
        <img src="images/${name}.png" width="100" onerror="this.src='images/noimage.png'">
      `;

      // -----------------------------
      // クリック時の処理
      // -----------------------------
      div.onclick = () => {
        // 状態を反転
        unlocked[name] = !unlocked[name];

        // クラス切り替え
        div.classList.toggle("locked");
        div.classList.toggle("unlocked");

        // 保存
        localStorage.setItem("unlocked", JSON.stringify(unlocked));
         // クリックでコンプ率更新
  updateCompletion();

        // 簡易クリックアニメーション
        div.animate([
          { transform: "scale(1)" },
          { transform: "scale(1.2)" },
          { transform: "scale(1)" }
        ], {
          duration: 200
        });
      };

      container.appendChild(div);
    });

updateCompletion();
}

// -----------------------------
// 検索対応
// -----------------------------
search.addEventListener("input", () => {
  renderList(search.value);
});

// -----------------------------
// 最初の表示
// -----------------------------
renderList();
